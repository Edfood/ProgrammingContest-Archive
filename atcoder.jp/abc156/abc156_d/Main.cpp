  mint ans = 1;
  ll t = n;
  mint cur = 2;
  
  while (t > 0) {
    if(t % 2) ans *= cur;
    cur *= cur;
    t /= 2;
  }
  ans -= 1;
  ans -= choose(n, a) + choose(n, b);
